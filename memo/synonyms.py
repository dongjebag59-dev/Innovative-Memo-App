SYNONYM_MAP = {
    "비번":     ["비밀번호", "password"],
    "비밀번호":  ["비번"],
    "password": ["비번", "비밀번호"],
    "아이디":   ["id", "계정"],
    "id":       ["아이디", "계정"],
    "계정":     ["아이디"],
    "핸드폰":   ["전화번호", "폰번호"],
    "전화번호":  ["핸드폰", "폰번호"],
    "폰번호":   ["전화번호", "핸드폰"],
    "이메일":   ["메일", "email"],
    "메일":     ["이메일"],
    "email":    ["이메일", "메일"],
    "통장":     ["계좌", "계좌번호"],
    "계좌":     ["통장"],
    "계좌번호":  ["통장", "계좌"],
    "카드":     ["카드번호"],
    "카드번호":  ["카드"],
    "택배":     ["배송", "배송지"],
    "배송":     ["택배"],
    "주소":     ["배송지"],
    "배송지":   ["주소", "배송"],
}


def expand_query(q: str) -> list[str]:
    q = q.strip()
    if not q:
        return []
    terms = {q}
    if q.lower() in SYNONYM_MAP:
        terms.update(SYNONYM_MAP[q.lower()])
    return list(terms)
